package modelo.vo.datosBasicos;

import java.util.Date;

/**
 *
 * @author Victor Del Rio Prens
 */
public class FuenteDeIngresosVO {
    private int codigo;
    private String nombre;
    private String usuario;
    private Date fecha;

    public FuenteDeIngresosVO() {
    }
    
    public FuenteDeIngresosVO(int codigo, String nombre) {
        this.codigo = codigo;
        this.nombre = nombre;
    }

    public FuenteDeIngresosVO(String nombre) {
        this.nombre = nombre;
    }
    
    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getUsuario() {
        return usuario;
    }

    public void setUsuario(String usuario) {
        this.usuario = usuario;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

}
