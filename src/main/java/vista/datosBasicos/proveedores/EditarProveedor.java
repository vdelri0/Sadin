    public ProveedorVO obtenerProveedor(){
        ProveedorVO proveedor = new ProveedorVO();
        try {
            proveedor.setCodigo(Integer.parseInt(campoCodigo.getText()));
            proveedor.setNombre(campoNombre.getText());
            proveedor.setNit(campoNit.getText());
            proveedor.setDireccion(campoDireccion.getText());
            proveedor.setTelefonos(campoTelefonos.getText());
            proveedor.setRepresentante(campoRepresentante.getText());
            proveedor.setEmail(campoEmail.getText());
            proveedor.setFax(campoFax.getText());
            proveedor.setCuentaContable(campoCuentaContable.getText());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, Constantes.codigoValido);
        }
        return proveedor;
    }

