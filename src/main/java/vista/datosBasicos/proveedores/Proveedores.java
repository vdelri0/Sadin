    /**
     * Permite crear una fila dentro de la tabla
     * @param codigo
     * @param nombre
     * @param nit
     * @param direccion
     * @param telefonos
     * @param representante
     * @param email
     * @param fax
     * @param cuentaContable
     */
    public void crearFila(int codigo, String nombre, String nit, String direccion, String telefonos, String representante, String email, String fax, String cuentaContable){
        model.addRow(new Object[]{codigo, nombre, nit, direccion, telefonos, representante, email, fax, cuentaContable});
    }
    
    /**
     * Toma el ultimo proveedor seleccionado dentro de la tabla y lo retorna
     * @return 
     */
    public ProveedorVO obtenerProveedorSeleccionado(){
        ProveedorVO proveedorSeleccionado = null;
        int index = tablaProveedores.getSelectedRow();
        if(index != -1){
            TableModel tableModel = tablaProveedores.getModel();
            proveedorSeleccionado = new ProveedorVO();
            proveedorSeleccionado.setCodigo(Integer.parseInt(tableModel.getValueAt(index, 0).toString()));
            proveedorSeleccionado.setNombre(tableModel.getValueAt(index, 1).toString());
            proveedorSeleccionado.setNit(tableModel.getValueAt(index, 2).toString());
            proveedorSeleccionado.setDireccion(tableModel.getValueAt(index, 3).toString());
            proveedorSeleccionado.setTelefonos(tableModel.getValueAt(index, 4).toString());
            proveedorSeleccionado.setRepresentante(tableModel.getValueAt(index, 5).toString());
            proveedorSeleccionado.setEmail(tableModel.getValueAt(index, 6).toString());
            proveedorSeleccionado.setFax(tableModel.getValueAt(index, 7).toString());
            proveedorSeleccionado.setCuentaContable(tableModel.getValueAt(index, 8).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunProveedorSeleccionado);
        }
        return proveedorSeleccionado;
    }
    
    /**
     * Toma todos los proveedores que se encuentran dentro de la base de datos y 
     * las ingresa en la tabla
     */
    public void llenarTabla(){
        Formulario.eliminarTodasLasFilas(model);
        List<ProveedorVO> proveedores = coordinador.buscarTodosLosProveedores();
        if(!proveedores.isEmpty()){
            for (ProveedorVO proveedor : proveedores) {
                crearFila(proveedor.getCodigo(), proveedor.getNombre(), proveedor.getNit(), proveedor.getDireccion(), proveedor.getTelefonos(), proveedor.getRepresentante(), proveedor.getEmail(), proveedor.getFax(), proveedor.getCuentaContable());
            }
        } else {
            JOptionPane.showMessageDialog(null, noHayProveedores);
        }
    }
    
