    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        int fila = tablaProveedores.convertRowIndexToModel(tablaProveedores.getSelectedRow());
        if(fila != -1){
            TableModel modelo = tablaProveedores.getModel();
            EditarProveedor editarProveedor = new EditarProveedor();
            editarProveedor.setVisible(true);
            editarProveedor.setCoordinador(coordinador);
            editarProveedor.getCampoCodigo().setText(modelo.getValueAt(fila, 0).toString());
            editarProveedor.getCampoNombre().setText(modelo.getValueAt(fila, 1).toString());
            editarProveedor.getCampoNit().setText(modelo.getValueAt(fila, 2).toString());
            editarProveedor.getCampoDireccion().setText(modelo.getValueAt(fila, 3).toString());
            editarProveedor.getCampoTelefonos().setText(modelo.getValueAt(fila, 4).toString());
            editarProveedor.getCampoRepresentante().setText(modelo.getValueAt(fila, 5).toString());
            editarProveedor.getCampoEmail().setText(modelo.getValueAt(fila, 6).toString());
            editarProveedor.getCampoFax().setText(modelo.getValueAt(fila, 7).toString());
            editarProveedor.getCampoCuentaContable().setText(modelo.getValueAt(fila, 8).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunProveedorSeleccionado);
        }
    }//GEN-LAST:event_botonEditarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        ProveedorVO proveedor = obtenerProveedorSeleccionado();
        if(proveedor != null){
            int confirmacion = JOptionPane.showConfirmDialog(null, confirmacionEliminar);
            if(JOptionPane.YES_OPTION == confirmacion){
                boolean eliminada = coordinador.eliminarProveedor(proveedor.getCodigo());
                if(eliminada){
                    int fila = tablaProveedores.convertRowIndexToModel(tablaProveedores.getSelectedRow());
                    model = (DefaultTableModel)tablaProveedores.getModel();
                    model.removeRow(fila);
                    JOptionPane.showMessageDialog(null, proveedorEliminado);
                    llenarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, proveedorNoEliminado);
                }
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

