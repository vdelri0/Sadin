     /**
     * Permite crear una fila dentro de la tabla
     * @param codigo
     * @param porcentaje
     * @param aplicarParaTodos
     */
    public void crearFila(int codigo, int porcentaje, char aplicarParaTodos){
        model.addRow(new Object[]{codigo, porcentaje, aplicarParaTodos});
    }
    
    /**
     * Toma el ultimo parametro IVA seleccionado dentro de la tabla y la retorna
     * @return 
     */
    public ParametrosIvaVO obtenerParametrosIVASeleccionado(){
        ParametrosIvaVO parametroIVASeleccionado = null;
        int index = tablaParametrosIVA.getSelectedRow();
        if(index != -1){
            TableModel tableModel = tablaParametrosIVA.getModel();
            parametroIVASeleccionado = new ParametrosIvaVO();
            parametroIVASeleccionado.setCodigo(Integer.parseInt(tableModel.getValueAt(index, 0).toString()));
            parametroIVASeleccionado.setPorcentaje(Integer.parseInt(tableModel.getValueAt(index, 1).toString()));
            parametroIVASeleccionado.setAplicarParaTodos(tableModel.getValueAt(index, 2).toString().charAt(0));
        } else {
            JOptionPane.showMessageDialog(null, ningunParametroIVASeleccionado);
        }
        return parametroIVASeleccionado;
    }
    
    /**
     * Toma todos los parametros IVA que se encuentran dentro de la base de datos y 
     * las ingresa en la tabla
     */
    public void llenarTabla(){
        Formulario.eliminarTodasLasFilas(model);
        List<ParametrosIvaVO> parametrosIVA = coordinador.buscarTodosLosParametrosIva();
        if(!parametrosIVA.isEmpty()){
            for (ParametrosIvaVO parametroIVA : parametrosIVA) {
                crearFila(parametroIVA.getCodigo(), parametroIVA.getPorcentaje(), parametroIVA.getAplicarParaTodos());
            }
        } else {
            JOptionPane.showMessageDialog(null, noHayParametrosIVA);
        }
    }
    
