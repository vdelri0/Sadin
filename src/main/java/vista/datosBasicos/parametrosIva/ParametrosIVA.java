    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        ParametrosIvaVO parametroIVA = obtenerParametrosIVASeleccionado();
        if(parametroIVA != null){
            int confirmacion = JOptionPane.showConfirmDialog(null, confirmacionEliminar);
            if(JOptionPane.YES_OPTION == confirmacion){
                boolean eliminada = coordinador.eliminarParametrosIva(parametroIVA.getCodigo());
                if(eliminada){
                    int fila = tablaParametrosIVA.convertRowIndexToModel(tablaParametrosIVA.getSelectedRow());
                    model = (DefaultTableModel)tablaParametrosIVA.getModel();
                    model.removeRow(fila);
                    JOptionPane.showMessageDialog(null, parametroIVAEliminado);
                    llenarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, parametroIVANoEliminado);
                }
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

