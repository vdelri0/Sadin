    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        int fila = tablaParametrosIVA.convertRowIndexToModel(tablaParametrosIVA.getSelectedRow());
        if(fila != -1){
            TableModel modelo = tablaParametrosIVA.getModel();
            EditarParametrosIVA editarParametroIVA = new EditarParametrosIVA();
            editarParametroIVA.setVisible(true);
            editarParametroIVA.setCoordinador(coordinador);
            editarParametroIVA.getCampoCodigo().setText(modelo.getValueAt(fila, 0).toString());
            editarParametroIVA.getCampoPorcentaje().setText(modelo.getValueAt(fila, 1).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunParametroIVASeleccionado);
        }
    }//GEN-LAST:event_botonEditarActionPerformed

