    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        int fila = tablaCajeros.convertRowIndexToModel(tablaCajeros.getSelectedRow());
        if(fila != -1){
            TableModel modelo = tablaCajeros.getModel();
            EditarCajero editarCajero = new EditarCajero();
            editarCajero.setVisible(true);
            editarCajero.setCoordinador(coordinador);
            editarCajero.getCampoCodigo().setText(modelo.getValueAt(fila, 0).toString());
            editarCajero.getCampoNombre().setText(modelo.getValueAt(fila, 1).toString());
            editarCajero.getCampoSiglas().setText(modelo.getValueAt(fila, 2).toString());
            editarCajero.getCampoPrefijo().setText(modelo.getValueAt(fila, 3).toString());
            editarCajero.getCampoRangoInicial().setText(modelo.getValueAt(fila, 4).toString());
            editarCajero.getCampoRangoFinal().setText(modelo.getValueAt(fila, 5).toString());
            editarCajero.getCampoUltimoConsecutivo().setText(modelo.getValueAt(fila, 6).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunCajeroSeleccionado);
        }
    }//GEN-LAST:event_botonEditarActionPerformed

    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        CajeroVO cajero = obtenerCajeroSeleccionado();
        if(cajero != null){
            int confirmacion = JOptionPane.showConfirmDialog(null, confirmacionEliminar);
            if(JOptionPane.YES_OPTION == confirmacion){
                boolean eliminada = coordinador.eliminarCajero(cajero.getCodigo());
                if(eliminada){
                    int fila = tablaCajeros.convertRowIndexToModel(tablaCajeros.getSelectedRow());
                    model = (DefaultTableModel)tablaCajeros.getModel();
                    model.removeRow(fila);
                    JOptionPane.showMessageDialog(null, cajeroEliminado);
                    llenarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, cajeroNoEliminado);
                }
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

