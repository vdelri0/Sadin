    /**
     * Permite crear una fila dentro de la tabla
     * @param codigo
     * @param nombre
     * @param siglas
     * @param prefijo
     * @param rangoInicial
     * @param rangoFinal
     * @param ultimoConsecutivo
     */
    public void crearFila(int codigo, String nombre, String siglas, String prefijo, String rangoInicial, String rangoFinal, String ultimoConsecutivo){
        model.addRow(new Object[]{codigo, nombre, siglas, prefijo, rangoInicial, rangoFinal, ultimoConsecutivo});
    }
    
    /**
     * Toma el ultimo cajero seleccionado dentro de la tabla y lo retorna
     * @return 
     */
    public CajeroVO obtenerCajeroSeleccionado(){
        CajeroVO cajeroSeleccionado = null;
        int index = tablaCajeros.getSelectedRow();
        if(index != -1){
            TableModel tableModel = tablaCajeros.getModel();
            cajeroSeleccionado = new CajeroVO();
            cajeroSeleccionado.setCodigo(Integer.parseInt(tableModel.getValueAt(index, 0).toString()));
            cajeroSeleccionado.setNombre(tableModel.getValueAt(index, 1).toString());
            cajeroSeleccionado.setSiglas(tableModel.getValueAt(index, 2).toString());
            cajeroSeleccionado.setPrefijo(tableModel.getValueAt(index, 3).toString());
            cajeroSeleccionado.setRangoInicial(tableModel.getValueAt(index, 4).toString());
            cajeroSeleccionado.setRangoFinal(tableModel.getValueAt(index, 5).toString());
            cajeroSeleccionado.setUltimoConsecutivo(tableModel.getValueAt(index, 6).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunCajeroSeleccionado);
        }
        return cajeroSeleccionado;
    }
    
    /**
     * Toma todos los cajeros que se encuentran dentro de la base de datos y 
     * las ingresa en la tabla
     */
    public void llenarTabla(){
        Formulario.eliminarTodasLasFilas(model);
        List<CajeroVO> cajeros = coordinador.buscarTodosLosCajeros();
        if(!cajeros.isEmpty()){
            for (CajeroVO cajero : cajeros) {
                crearFila(cajero.getCodigo(), cajero.getNombre(), cajero.getSiglas(), cajero.getPrefijo(), cajero.getRangoInicial(), cajero.getRangoFinal(), cajero.getUltimoConsecutivo());
            }
        } else {
            JOptionPane.showMessageDialog(null, noHayCajeros);
        }
    }
    
