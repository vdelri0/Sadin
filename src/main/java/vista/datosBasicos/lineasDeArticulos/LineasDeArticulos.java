    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        LineaDeArticulosVO lineaDeArticulos = obtenerLineaDeArticulosSeleccionada();
        if(lineaDeArticulos != null){
            int confirmacion = JOptionPane.showConfirmDialog(null, confirmacionEliminar);
            if(JOptionPane.YES_OPTION == confirmacion){
                boolean eliminada = coordinador.eliminarLineaDeArticulos(lineaDeArticulos.getCodigo());
                if(eliminada){
                    int fila = tablaLineasArticulos.convertRowIndexToModel(tablaLineasArticulos.getSelectedRow());
                    model = (DefaultTableModel)tablaLineasArticulos.getModel();
                    model.removeRow(fila);
                    JOptionPane.showMessageDialog(null, lineaDeArticulosEliminada);
                    llenarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, lineaDeArticulosNoEliminada);
                }
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

