    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        int fila = tablaLineasArticulos.convertRowIndexToModel(tablaLineasArticulos.getSelectedRow());
        if(fila != -1){
            TableModel modelo = tablaLineasArticulos.getModel();
            EditarLineaDeArticulos editarLineaDeArticulos = new EditarLineaDeArticulos();
            editarLineaDeArticulos.setVisible(true);
            editarLineaDeArticulos.setCoordinador(coordinador);
            editarLineaDeArticulos.getCampoCodigo().setText(modelo.getValueAt(fila, 0).toString());
            editarLineaDeArticulos.getCampoDescripcion().setText(modelo.getValueAt(fila, 1).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunaLineaDeArticulosSeleccionada);
        }
    }//GEN-LAST:event_botonEditarActionPerformed

