    /**
     * Permite crear una fila dentro de la tabla
     * @param codigo
     * @param descripcion
     */
    public void crearFila(int codigo, String descripcion){
        model.addRow(new Object[]{codigo, descripcion});
    }
    
    /**
     * Toma la ultima linea de articulos seleccionada dentro de la tabla y la retorna
     * @return 
     */
    public LineaDeArticulosVO obtenerLineaDeArticulosSeleccionada(){
        LineaDeArticulosVO lineaDeArticulosSeleccionada = null;
        int index = tablaLineasArticulos.getSelectedRow();
        if(index != -1){
            TableModel tableModel = tablaLineasArticulos.getModel();
            lineaDeArticulosSeleccionada = new LineaDeArticulosVO();
            lineaDeArticulosSeleccionada.setCodigo(Integer.parseInt(tableModel.getValueAt(index, 0).toString()));
            lineaDeArticulosSeleccionada.setDescripcion(tableModel.getValueAt(index, 1).toString());
            
        } else {
            JOptionPane.showMessageDialog(null, ningunaLineaDeArticulosSeleccionada);
        }
        return lineaDeArticulosSeleccionada;
    }
    
    /**
     * Toma todas las lineas de articulos que se encuentran dentro de la base de datos y 
     * las ingresa en la tabla
     */
    public void llenarTabla(){
        Formulario.eliminarTodasLasFilas(model);
        List<LineaDeArticulosVO> lineasDeArticulos = coordinador.buscarTodasLasLineasDeArticulos();
        if(!lineasDeArticulos.isEmpty()){
            for (LineaDeArticulosVO lineaDeArticulos : lineasDeArticulos) {
                crearFila(lineaDeArticulos.getCodigo(), lineaDeArticulos.getDescripcion());
            }
        } else {
            JOptionPane.showMessageDialog(null, noHayLineasDeArticulos);
        }
    }

