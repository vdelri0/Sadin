    private void botonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEliminarActionPerformed
        FuenteDeIngresosVO fuenteDeIngresos = obtenerFuenteDeIngresosSeleccionada();
        if(fuenteDeIngresos != null){
            int confirmacion = JOptionPane.showConfirmDialog(null, confirmacionEliminar);
            if(JOptionPane.YES_OPTION == confirmacion){
                boolean eliminada = coordinador.eliminarFuenteDeIngresos(fuenteDeIngresos.getCodigo());
                if(eliminada){
                    int fila = tablaFuentesDeIngresos.convertRowIndexToModel(tablaFuentesDeIngresos.getSelectedRow());
                    model = (DefaultTableModel)tablaFuentesDeIngresos.getModel();
                    model.removeRow(fila);
                    JOptionPane.showMessageDialog(null, fuenteDeIngresosEliminada);
                    llenarTabla();
                } else {
                    JOptionPane.showMessageDialog(null, fuenteDeIngresosNoEliminada);
                }
            }
        }
    }//GEN-LAST:event_botonEliminarActionPerformed

