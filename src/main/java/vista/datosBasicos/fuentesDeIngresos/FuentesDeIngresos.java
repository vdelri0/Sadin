    private void botonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonEditarActionPerformed
        int fila = tablaFuentesDeIngresos.convertRowIndexToModel(tablaFuentesDeIngresos.getSelectedRow());
        if(fila != -1){
            TableModel modelo = tablaFuentesDeIngresos.getModel();
            EditarFuenteDeIngresos editarFuenteDeIngresos = new EditarFuenteDeIngresos();
            editarFuenteDeIngresos.setVisible(true);
            editarFuenteDeIngresos.setCoordinador(coordinador);
            editarFuenteDeIngresos.getCampoCodigo().setText(modelo.getValueAt(fila, 0).toString());
            editarFuenteDeIngresos.getCampoNombre().setText(modelo.getValueAt(fila, 1).toString());
        } else {
            JOptionPane.showMessageDialog(null, ningunaFuenteDeIngresosSeleccionada);
        }
    }//GEN-LAST:event_botonEditarActionPerformed

